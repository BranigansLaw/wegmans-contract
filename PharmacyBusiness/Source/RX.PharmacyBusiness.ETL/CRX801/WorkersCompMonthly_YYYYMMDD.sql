Select FACILITY_ID_#, RX_#, DATE_FILLED, PATIENT_LAST_NAME, PATIENT_FIRST_NAME, CARDHOLDER_ID, PATIENT_DOB, DRUG_NDC_#, DRUG_NAME, QTY_DRUG_DISPENSED, TX_PRICE, PATIENT_PAY, ADJ_AMT, THIRD_PARTY_NAME, THIRD_PARTY_CODE, SPLIT_BILL_INDICATOR, PRESCRIBER_NAME, DATE_SOLD, CLAIM_NUM 
from (
      SELECT      F.FD_FACILITY_ID AS FACILITY_ID_#,
                  FF.RX_NUMBER AS RX_#,
                  TO_CHAR(DD.CAL_DATE, 'MM/DD/YYYY') AS DATE_FILLED,
                  PAT.PD_LAST_NAME AS PATIENT_LAST_NAME,
                  PAT.PD_FIRST_NAME AS PATIENT_FIRST_NAME,
                  TPAT.TPD_CARDHOLDER_ID AS CARDHOLDER_ID,
                  TO_CHAR(PAT.PD_BIRTH_DATE, 'MM/DD/YYYY') AS PATIENT_DOB,
                  D.DRUG_NDC_MFG || '-' || D.DRUG_NDC_PRODUCT || '-' || D.DRUG_NDC_SIZE AS DRUG_NDC_#,
                  PRD.PRD_NAME AS DRUG_NAME,
                  CEIL(FF.QTY_DISPENSED) AS QTY_DRUG_DISPENSED,
                  (NVL(FPF.NON_COVERED_AMT, 0.00) + NVL(FPF.ACTUAL_PAID_AMT, 0)) AS TX_PRICE,
                   NVL(FPF.NON_COVERED_AMT, 0.00) AS PATIENT_PAY,
                   NVL(FPF.ACTUAL_PAID_AMT, 0.00) AS ADJ_AMT,
                   TPP.TPLD_PLAN_NAME AS THIRD_PARTY_NAME,
                   TPP.TPLD_PLAN_CODE AS THIRD_PARTY_CODE,
                   DECODE(FF.IS_COB,'Y','1','0') AS SPLIT_BILL_INDICATOR,
                   DECODE(PRES.PRS_MIDDLE_NAME, NULL,
                          PRES.PRS_LAST_NAME || ', ' || PRES.PRS_FIRST_NAME,
                          PRES.PRS_LAST_NAME || ', ' || PRES.PRS_FIRST_NAME || ' ' || PRES.PRS_MIDDLE_NAME) AS PRESCRIBER_NAME
                   ,DD2.CAL_DATE AS DATE_SOLD       
                   ,TWC.TWED_TP_WRKRS_CMP_CLM_NUM AS CLAIM_NUM
                   ,DD2.cal_year_month_name
                   ,FF.CANCEL_DATE_KEY
     FROM TREXONE_DW_DATA.FILL_FACT FF
          INNER JOIN TREXONE_DW_DATA.FILL_PRICING_FACT FPF ON (FF.PRIMARY_TPLD_PLAN_KEY = FPF.TPLD_PLAN_KEY AND FF.ADJUDICATION_GROUP_NUM = FPF.ADJUDICATION_GROUP_NUM)
          INNER JOIN TREXONE_DW_DATA.TP_PLAN TPP ON FPF.TPLD_PLAN_KEY = TPP.TPLD_PLAN_KEY AND TPP.TPLD_PLAN_CODE IN ('OTHWFMWC', 'WEGCOMP', 'WEGCOMP1')
          INNER JOIN TREXONE_DW_DATA.FACILITY F ON FF.FD_FACILITY_KEY = F.FD_FACILITY_KEY
          INNER JOIN TREXONE_DW_DATA.DATE_DIM DD ON FPF.DATE_OF_SERVICE_DATE_KEY = DD.DATE_KEY
          INNER JOIN TREXONE_DW_DATA.PATIENT PAT ON FF.PD_PATIENT_KEY = PAT.PD_PATIENT_KEY
          INNER JOIN TREXONE_DW_DATA.TP_PATIENT TPAT ON FPF.TPD_KEY = TPAT.TPD_KEY AND TPAT.TPD_TP_PLAN_NUM = TPP.TPLD_PLAN_NUM
          INNER JOIN TREXONE_DW_DATA.DRUG D ON FF.DISPENSED_DRUG_KEY = D.DRUG_KEY
          INNER JOIN TREXONE_DW_DATA.PRESCRIBER PRES ON FF.PRS_PRESCRIBER_KEY = PRES.PRS_PRESCRIBER_KEY
          INNER JOIN TREXONE_DW_DATA.PRODUCT PRD ON FF.DISPENSED_PRD_PRODUCT_KEY = PRD.PRD_PRODUCT_KEY              
          INNER JOIN TREXONE_DW_DATA.TP_WORKERS_COMP TWC ON TWC.TWED_KEY = FPF.TWED_KEY 
          INNER JOIN TREXONE_DW_DATA.DATE_DIM DD2 ON FPF.SOLD_DATE_KEY = DD2.DATE_KEY
     )
WHERE cal_year_month_name = To_Char((To_Date(To_Char(:RunDate,'YYYYMM') || '01','YYYYMMDD') - 1),'YYYY-MM')
  AND CANCEL_DATE_KEY = 0 -- Exclude Reversals